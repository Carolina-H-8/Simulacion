ax1.legend(loc='upper left')

# 2. Análisis de Tendencia (Media Móvil)
df['Media_Movil'] = df['Vibracion_Hz'].rolling(window=5).mean()
ax1.plot(df['Hora'], df['Media_Movil'], color='white', linestyle='--', label='Tendencia (MA5)')

