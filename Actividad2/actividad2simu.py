# --- GENERACIÓN DEL MAPA DE CALOR DE VALIDACIÓN ---

def graficar_validacion(df, modelo):
    plt.style.use('default') # Volvemos a estilo claro para mejor lectura de etiquetas
    plt.figure(figsize=(6, 4))

    # Creamos la tabla cruzada entre Realidad y Sensor
    # Convertimos los índices numéricos a nombres para que sea legible
    real_nombres = [modelo.estados[i] for i in df['Estado_Real']]
    sensor_nombres = [modelo.obs_nombres[i] for i in df['Obs_Idx']]

    matriz_confusion = pd.crosstab(
        pd.Series(real_nombres, name='ESTADO REAL (OCULTO)'),
        pd.Series(sensor_nombres, name='DETECCIÓN SENSOR (OBSERVACIÓN)'),
        normalize='index' # Esto nos da porcentajes, útil para validación
    )

    # Dibujar el Mapa de Calor
    sns.heatmap(matriz_confusion, annot=True, fmt=".2%", cmap="YlOrRd", cbar_kws={'label': 'Probabilidad de Coincidencia'})

    plt.title("MATRIZ DE VALIDACIÓN HMM\n(¿Qué tan bien el sensor representa la realidad?)", fontsize=14, pad=20)
    plt.show()

# Llamamos a la función con los datos de tu última simulación
graficar_validacion(df, modelo)


plt.tight_layout()
plt.show()