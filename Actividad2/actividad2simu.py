import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

class MantenimientoHMM:
    def __init__(self):
        self.estados = ['Normal', 'Desgaste', 'Falla']
        self.obs_nombres = ['Baja', 'Media', 'Alta']

        # 1. Especificaciones del Modelo
        self.pi = np.array([0.80, 0.15, 0.05])
        self.transicion = np.array([
            [0.85, 0.13, 0.02],#normal
            [0.30, 0.50, 0.20],#desgaste
            [0.05, 0.15, 0.80]#falla
        ])
        self.emision = np.array([ ## el sensor
            [0.80, 0.15, 0.05],#normal
            [0.30, 0.50, 0.20],#desgaste
            [0.10, 0.30, 0.60]#falla
        ])

    def generar_hz(self, obs_idx):
        # Mapeo de categorías a valores continuos de Hz
        rangos = [(10, 30), (35, 65), (70, 110)]#bajo(10, 30) #media(35, 65) #alta(70, 110)
        return np.random.uniform(rangos[obs_idx][0], rangos[obs_idx][1])

    def simular(self, n_pasos=60):
        historial_estados = []
        historial_obs = []
        vibracion_hz = []

        estado_actual = np.random.choice([0, 1, 2], p=self.pi)

        for _ in range(n_pasos):
            obs_idx = np.random.choice([0, 1, 2], p=self.emision[estado_actual])
            historial_estados.append(estado_actual)
            historial_obs.append(obs_idx)
            vibracion_hz.append(self.generar_hz(obs_idx))

            estado_actual = np.random.choice([0, 1, 2], p=self.transicion[estado_actual])

        return pd.DataFrame({
            'Hora': range(1, n_pasos + 1),
            'Estado_Real': historial_estados,
            'Obs_Idx': historial_obs,
            'Vibracion_Hz': vibracion_hz
        })

# --- EJECUCIÓN Y MÉTRICAS ---
modelo = MantenimientoHMM()
df = modelo.simular(60)

# Calcular RUL (Vida Útil Restante estimada simplificada)
# Si la tasa de falla es 0.02 en normal, la vida media es 1/p
mtbf_estimado = 1 / 0.15 # Probabilidad de salir de estado Normal/Estable
falla_detectada = df[df['Estado_Real'] == 2]['Hora'].min()
rul = falla_detectada - df['Hora'].max() if not pd.isna(falla_detectada) else mtbf_estimado

# --- DASHBOARD INDUSTRIAL ---
plt.style.use('dark_background')
fig = plt.figure(figsize=(16, 10))
