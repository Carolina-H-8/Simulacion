ax1 = fig.add_subplot(gs[0, :])
ax1.plot(df['Hora'], df['Vibracion_Hz'], color='cyan', lw=2, label='Vibración (Hz)')
ax1.axhspan(0, 35, color='green', alpha=0.2, label='Zona Normal')
